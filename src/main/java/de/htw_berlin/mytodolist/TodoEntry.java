package de.htw_berlin.mytodolist;

public class TodoEntry {
    String title;
    boolean done;

    public TodoEntry() {}

    public TodoEntry(String title) {
        this.title = title;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public boolean isDone() {
        return done;
    }

    public void setDone(boolean done) {
        this.done = done;
    }
}
